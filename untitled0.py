"""
Preprocessing Answers

1. Why do you augment only the training set?

Data augmentation is used only on the training set because
its purpose is to create additional variations of training
examples and improve generalization.

Validation and test sets must remain unchanged so they
represent real-world unseen data. If augmentation is applied
to validation or test data, evaluation results become
unreliable and no longer reflect actual model performance.

2. Why normalize pixel values to [0,1]?

Raw image pixels range from 0 to 255.

Large values can slow training and cause unstable gradients.
Dividing by 255 scales all values into the range [0,1],
which improves numerical stability and helps neural networks
converge faster.

3. How did you handle class imbalance?

The dataset contains more Pneumonia images than Normal images.

To address imbalance:
- Use class weights during training.
- Monitor Recall and F1 score in addition to Accuracy.
- Apply augmentation to improve generalization.

Recall is especially important because false negatives are
dangerous in medical diagnosis.
"""

# ==========================================
# SECTION 4 - MODEL ARCHITECTURE
# ==========================================

from tensorflow import keras

# ------------------------------------------
# Transfer Learning Backbone
# ------------------------------------------

base = keras.applications.MobileNetV2(
    input_shape=(150, 150, 3),
    include_top=False,
    weights="imagenet"
)

# Freeze pretrained weights
base.trainable = False

# ------------------------------------------
# Classification Head
# ------------------------------------------

model = keras.Sequential([

    # Feature extractor
    base,

    # Convert feature maps to vector
    keras.layers.GlobalAveragePooling2D(),

    # Stabilize activations
    keras.layers.BatchNormalization(),

    # Regularization
    keras.layers.Dropout(0.30),

    # Dense classifier
    keras.layers.Dense(
        128,
        activation="relu"
    ),

    keras.layers.Dropout(0.20),

    # Binary output
    keras.layers.Dense(
        1,
        activation="sigmoid"
    )
])

model.summary()

print("\nTotal Parameters:")
print(f"{model.count_params():,}")

# ==========================================
# SECTION 5 - TRAINING
# ==========================================

from tensorflow import keras
from tensorflow.keras.callbacks import (
    EarlyStopping,
    ModelCheckpoint,
    ReduceLROnPlateau,
    CSVLogger
)

import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

# ==========================================
# Callbacks
# ==========================================

callbacks = [

    EarlyStopping(
        monitor="val_loss",
        patience=10,
        restore_best_weights=True,
        verbose=1
    ),

    ModelCheckpoint(
        "best_model.keras",
        monitor="val_loss",
        save_best_only=True,
        verbose=1
    ),

    ReduceLROnPlateau(
        monitor="val_loss",
        factor=0.5,
        patience=4,
        min_lr=1e-7,
        verbose=1
    ),

    CSVLogger("training_log.csv")
]

# ==========================================
# Compile Model
# ==========================================

model.compile(

    optimizer=keras.optimizers.Adam(
        learning_rate=0.0001
    ),

    loss="binary_crossentropy",

    metrics=[
        "accuracy",
        keras.metrics.AUC(name="auc"),
        keras.metrics.Recall(name="recall"),
        keras.metrics.Precision(name="precision")
    ]
)

# ==========================================
