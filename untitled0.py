# ==========================================

class DeepLearningPredictor:

    def __init__(
        self,
        model_path,
        metadata_path,
        labels_path
    ):

        self.model = keras.models.load_model(
            model_path
        )

        with open(metadata_path) as f:
            self.meta = json.load(f)

        with open(labels_path) as f:
            self.labels = json.load(f)

        self.input_shape = tuple(
            self.meta["input_shape"]
        )

        logger.info(
            f"Loaded model. Test AUC = "
            f"{self.meta['metrics']['test_auc']}"
        )

    def preprocess_image(
        self,
        image_path
    ):

        img = keras.utils.load_img(
            image_path,
            target_size=self.input_shape[:2]
        )

        arr = keras.utils.img_to_array(
            img
        )

        arr = arr / 255.0

        return np.expand_dims(
            arr,
            axis=0
        )

    def predict(
        self,
        input_data
    ):

        try:

            if isinstance(
                input_data,
                str
            ):

                x = self.preprocess_image(
                    input_data
                )

            else:

                x = np.expand_dims(
                    input_data,
                    axis=0
                )

            probs = self.model.predict(
                x,
                verbose=0
            )[0]

            # Binary Classification

            pred_idx = int(
                probs[0] > 0.5
            )

            confidence = float(
                probs[0]
            )

            label = self.labels[
                str(pred_idx)
            ]

            result = {

                "prediction":
                    label,

                "confidence":
                    f"{confidence:.1%}",

                "timestamp":
                    datetime.now().isoformat()
            }

            logger.info(
                f"Prediction: {label}"
            )

            return result

        except Exception as e:

            logger.error(
                f"Prediction Error: {e}"
            )

            return {
                "error": str(e)
            }

    def predict_batch(
        self,
        inputs
    ):

        return [
            self.predict(x)
            for x in inputs
        ]


